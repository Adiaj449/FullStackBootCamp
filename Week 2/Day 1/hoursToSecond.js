function hoursToSecond(hours)
{
    return (hours * 3600);
}

console.log(hoursToSecond(2));
console.log(hoursToSecond(12));
console.log(hoursToSecond(10));
console.log(hoursToSecond(8));
console.log(hoursToSecond(6));
console.log(hoursToSecond(54));


