function firstEleArray(arr)
{
    return arr[0];
}

console.log(firstEleArray(arr = [2,3,4,5]));
console.log(firstEleArray(arr = [12,13,14,15]));
console.log(firstEleArray(arr = [42,434,44,45]));
console.log(firstEleArray(arr = [-112,33,547,875]));

